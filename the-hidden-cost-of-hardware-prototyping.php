              <h2 class="section_md_heading cstudy_chlng_head">1. Prototyping Cost</h2>
              <p class="bd_md_desc regular_font">The cost of a prototype depends on the stage of the product development journey, i.e., concept design, design engineering, prototype, and testing or manufacturing, the risk involved at each stage helps determine the prototyping cost for each stage of development.</p>

              <p class="bd_md_desc regular_font">In a conceptual design, the risk involved is user confusion, physical size constraints, interaction methods, etc. The type of prototypes that can work is protoboard, development board etc. For this stage, low material costs are off the shelf and include 3D custom prototype parts, module electronics, and others.In a conceptual design, the risk involved is user confusion, physical size constraints, interaction methods, etc. The type of prototypes that can work is protoboard, development board etc. For this stage, low material costs are off the shelf and include 3D custom prototype parts, module electronics, and others.</p>

              <p class="bd_md_desc regular_font">For the design stage, scale models of products and hand-built models can be used to assess the signal integrity, EMI/EMC, wire and sub-component management, etc. Here, the cost of the prototype depends on the customized sub-system needed and user testing validation metrics.</p>
