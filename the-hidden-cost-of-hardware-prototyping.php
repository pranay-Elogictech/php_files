              <h2 class="section_md_heading cstudy_chlng_head">3. Limitations in the Number of Iterations</h2>
              <p class="bd_md_desc regular_font">Iteration is the sequential testing and refinement of a prototype. It is a primary technique that leads to the gradual achievement of requirements and determines the appropriate component, size, shape, architecture, and performance improvement. Iteration is essential to meet challenging requirements, get insight into issues, identify errors and simplify parts. It leads to self-efficacy and design requirement satisfaction.</p>

              <p class="bd_md_desc regular_font">However, hardware prototyping has a limitation in the number of iterations compared to software development due to cost constraints. Each iteration and test come with a cost that may increase the value of the project.</p>
            </div>            
          </div>

